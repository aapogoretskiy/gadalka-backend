-- =============================================================================
-- Реферальная аналитика: список новых пользователей по источнику
-- =============================================================================
-- Подставьте конкретный код в WHERE-фильтр или уберите его, чтобы видеть всех.
-- =============================================================================

SELECT
    u.id                                                        AS "user_id",
    u.telegram_id,
    u.username,
    u.first_name                                                AS "Имя",
    u.referral_source                                           AS "Источник (из профиля)",
    u.created_at::DATE                                          AS "Дата регистрации"

FROM users u
WHERE u.referral_source IS NOT NULL
  -- AND u.referral_source = 'telegram_channel1'   -- раскомментируйте для фильтра по источнику
ORDER BY u.created_at DESC;
